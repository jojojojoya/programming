
public class Variable03 {
	public static void main(String[] args) {
		// 2. 문자 / 문자열(여러 자)
		// String

		// 이름 저장 >> 문자 저장한다고 하면 스트링 쓰면 됨
		String myName = "sj";
		System.out.println(myName);

		/*
		 	정수 : int
		 	소수 : double
		 	글자 : String
		 	논리 (참,거짓) : boolean
		 	
		 */

		// 내 개인정보
			// 이름, 나이, 키
		
		String name = "sj";
		int age = 28;
		double height = 161.3;
		
		System.out.println(name+".CHO");
		System.out.println(age+"세");
		System.out.println(height+"cm");
		
		// 코드 순서를 바꿔도 값은 동일 
		
		String phoneCat = "iphone6"; 	
		String madeby = "Apple";
		double phoneSize =5.6;
		int phonePrice = 1000;
		boolean inventory = true;
		
		System.out.println("핸드폰 정보 *********");
		System.out.println("모델명\t- "+phoneCat);
		System.out.println("제조사\t- "+madeby);
		System.out.println("디스플레이\t- "+phoneSize+"inch");
		System.out.println("가격\t- "+phonePrice+"$");
		System.out.println("재고유무\t- "+inventory);
		System.out.println("****************");
		
		// \t는 붙은 것 바로 앞을 정렬 
		
		
		//にほんごもできる～
		
		
		
		
		
	}
}
