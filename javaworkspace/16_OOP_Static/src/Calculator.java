
public class Calculator {
			// 필드 (속성)
			// 표현할 속성이 없음.
			// 필드를 정의할 필요 없음.
		
			// 객체를 만들 필요도 없음.
		
		// static 메서드. (==> 클래스 메서드: 올바른 명칭)
		public static void add(int a, int b) {
		System.out.println(a+b);
		}
		
		// static 없으면 객체(인스턴스)가 필요.	
		// static 있으면 객체 필요없이 자동완성 가능 	
		
		
		// 일반 메서드
			public static void addThree(int a, int b, int c) {
			System.out.println(a+b+c);
		
		
	}}
