
public class Test {
	public static void main(String[] args) {
		System.out.println("*****************");
		System.out.println("*이름\t: 수진	*\n");
		System.out.println();
		System.out.println("\n*나이	: 28	*");
		System.out.println("*사는곳	: 낙성대	*");
		System.out.println("*****************");

		
		// System.out : 표준 출력 공간
		// System.in : 표준 입력 공간
		
		// println : 출력 후 줄 바꿔줌
		// print : 출력 (줄 안바꿔줌)
		// printf : 출력 형식 잡을때 사용
		
		
		// \t : 탭 (정렬가능)
		// \n : 줄바꿈, 엔터키
		// ctrl + shift + f : 코드 정렬
		

		System.out.println("\n*****************\n*이름\t: 수진	*\n\n\n*나이	: 28	*\n*사는곳	: 낙성대	\n******************");

	}
}


